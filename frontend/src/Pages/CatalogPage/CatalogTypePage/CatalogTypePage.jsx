export default function CatalogTypePage({ typeSlug }) {
  const type = PROPERTY_TYPE_BY_SLUG[typeSlug];
  if (!type) return null;

  const items = getPropertiesByType(typeSlug);
  const editorial = EDITORIAL[typeSlug];

  return (
    <main className={styles.page}>
      <Container>
        <section className={styles.hero}>
          <h1 className={styles.heroTitle}>
            {type.isAll ? (
              <>
                Все{" "}
                <span className={styles.heroTitleUnderline}>новостройки</span>
                <br />
                ОАЭ в одном каталоге
              </>
            ) : (
              <>
                Все{" "}
                <span className={styles.heroTitleUnderline}>
                  {type.label.toLowerCase()}
                </span>
                <br />
                на продажу в ОАЭ
              </>
            )}
          </h1>

          <div className={styles.divider} />
          <div className={styles.descInner}>
            <span className={styles.descQuote}>&ldquo;</span>
            <p className={styles.descText}>{type.subtitle}</p>
            <span className={styles.descQuote}>&rdquo;</span>
          </div>

          <div className={styles.heroCatsWrap}>
            <div className={styles.heroCats}>
              {PROPERTY_TYPES.map((t) => {
                const Icon = TYPE_ICONS[t.slug];
                return (
                  <Link
                    key={t.slug}
                    href={`/${t.slug}`}
                    className={`${styles.heroCatChip} ${t.slug === typeSlug ? styles.heroCatChipActive : ""}`}
                  >
                    {Icon && <Icon size={15} />}
                    {t.label}
                  </Link>
                );
              })}
            </div>
          </div>
        </section>

        <PropertyFilter key={typeSlug} items={items} typeSlug={typeSlug} />
      </Container>

      {editorial && (
        <CatalogEditorial
          title={editorial.title}
          highlight={editorial.highlight}
          paragraphs={editorial.paragraphs}
        />
      )}
    </main>
  );
}
